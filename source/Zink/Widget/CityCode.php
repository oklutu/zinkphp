    public static function isSpecialCity($city)
    {
        if (!isset(self::$_specialCityCode[$city])) {
            return FALSE;
        }

       return self::$_specialCityCode[$city] ? TRUE : FALSE;
    }

    public static function isGeneralCity($city)
    {
        if (!isset(self::$_generalCityCode[$city])) {
            return FALSE;
        }

        return self::$_generalCityCode[$city] ? TRUE : FALSE;
    }

    public static function getCityCode($city, $isNeedPrefix = true)
    {
        if (!self::isGeneralCity($city)) {
            return isset(self::$_specialCityCode[$city]) ? self::$_specialCityCode[$city] : '';
        }

        return $isNeedPrefix ? self::$_pre .self::$_generalCityCode[$city] : self::$_generalCityCode[$city];
    }

   
